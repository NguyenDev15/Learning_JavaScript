// concat(), toUpperCase(), toLowerCase(), charAt();

// concat() => nối chuỗi
let ho = "Lionel";
let ten = "Messi";
let fullTen = ho.concat(" ", ten); // " " là dấu cách
console.log(`Tên của bạn là ${fullTen}`);

// toUpperCase -> in hoa

let toUpper = ten.toUpperCase();
console.log(toUpper);

// toLowerCase -> in thường

let toLower = ten.toLowerCase();
console.log(toLower);

// charAt() -> trả về giá trị index truyền vào

let charAt = ten.charAt(4);
console.log(charAt);